function mover1(){
    var circulo = document.getElementById('img1');
    var texto = document.getElementById('txt1');
    var paragrafo = document.getElementById('para1');

    if(paragrafo.style.left != '22%'){
        circulo.style.marginLeft = '-80%';
        circulo.style.transition = '2s';

        texto.style.marginTop = '-16%';
        texto.style.marginBottom ='16%';
        texto.style.marginLeft = '-44.6%';
        texto.style.transition = '2s';

        paragrafo.style.left = '22%';
        paragrafo.style.transition = '2s';
        paragrafo.style.transitionDelay = '0.5s';

        circulo.title = "Clique para ver menos!";
        texto.title = "Clique para ver menos!";
    }
    else{
        circulo.style.marginLeft = '0';
        circulo.style.transition = '1.7s';
        circulo.style.transitionDelay = "1.7s";

        texto.style.marginTop = '0';
        texto.style.marginBottom ='0';
        texto.style.marginLeft = '0';
        texto.style.transition = '2s';
        texto.style.transitionDelay = '1.2S';
        

        paragrafo.style.left = '-250%';
        paragrafo.style.transition = '4s';
        
        circulo.title = "Clique para ver mais!";
        circulo.title = "Clique para ver mais!";
    }
}

